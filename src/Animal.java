public class Animal {
    private String name;
    private int speed;
    protected int xVal;
    protected int yVal;


    public Animal(String name) {
        this.name = name;
        this.speed = speed;
    }

    public String getName() {
        return name;
    }

    public int getSpeed() {
        return speed;
    }

    public int getxVal() {
        return xVal;
    }

    public int getyVal() {
        return yVal;
    }
}
